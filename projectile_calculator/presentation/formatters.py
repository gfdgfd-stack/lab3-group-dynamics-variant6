"""Форматирование вещественных значений для Области_Результатов.

Этот модуль изолирует логику округления и преобразования десятичной
точки в запятую: парсинг ввода (запятая → точка) находится в
``Validator``, а вывод (точка → запятая) — здесь.

Ссылки на требования:
    - 3.7  — отображение L и H в Режиме_Без_Сопротивления с шагом 0.01 м.
    - 4.9  — отображение L и H в Режиме_С_Сопротивлением с шагом 0.01 м.
    - 5.4  — округление по правилу round-half-away-from-zero, запятая
             как десятичный разделитель, без разделителей разрядов.
"""

from __future__ import annotations

import math

__all__ = ["round_to_centimeter", "format_meters"]


def round_to_centimeter(x: float) -> float:
    """Округлить ``x`` до двух знаков после запятой по правилу
    round-half-away-from-zero.

    В отличие от стандартной функции :func:`round`, которая в Python
    использует банкирское округление (round-half-to-even), здесь
    значение 5 в отбрасываемом разряде всегда округляется в большую
    сторону по абсолютной величине (Req 5.4):

    >>> round_to_centimeter(0.005)
    0.01
    >>> round_to_centimeter(-0.005)
    -0.01

    Реализация повторяет рекомендацию из дизайн-документа:
    ``math.floor(abs(x) * 100 + 0.5) / 100`` с последующим
    восстановлением знака. Случай ``x == 0`` обрабатывается отдельно,
    чтобы вернуть положительный ноль (а не ``-0.0``) и тем самым
    избежать строки ``"-0,00"`` в :func:`format_meters` для значений,
    близких к нулю с отрицательной стороны.

    Args:
        x: исходное значение в метрах.

    Returns:
        Значение, округлённое до двух знаков после запятой.
    """
    if x == 0:
        return 0.0

    magnitude = math.floor(abs(x) * 100.0 + 0.5) / 100.0
    if magnitude == 0.0:
        # Для входов вроде -0.0001 округлённая величина — ноль; возвращаем
        # положительный 0.0, чтобы format_meters не давал "-0,00".
        return 0.0

    return math.copysign(magnitude, x)


def format_meters(x: float) -> str:
    """Сформатировать значение в метрах как строку вида ``"12,34"``.

    Используются ровно два знака после десятичного разделителя,
    разделитель — запятая, разделители тысячных разрядов отсутствуют
    (Req 5.4):

    >>> format_meters(0)
    '0,00'
    >>> format_meters(5)
    '5,00'
    >>> format_meters(1234.5)
    '1234,50'

    Округление выполняется через :func:`round_to_centimeter`, поэтому
    форматирование и округление согласованы между собой.

    Args:
        x: исходное значение в метрах.

    Returns:
        Строковое представление округлённого значения с запятой
        в качестве десятичного разделителя.
    """
    rounded = round_to_centimeter(x)
    return f"{rounded:.2f}".replace(".", ",")
